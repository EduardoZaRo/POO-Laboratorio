    private void entradanombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_entradanombreKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyChar()>='a'&&evt.getKeyChar()<='z'||evt.getKeyChar()>='A'&&evt.getKeyChar()<='Z'){
            
            nombre += evt.getKeyChar();
            System.out.println(evt.getKeyChar());
            evt.consume();       
        }
        else{
            JOptionPane.showMessageDialog(null, "Solo se permiten nombres con mayusculas y minusculas","Info importante XD",JOptionPane.INFORMATION_MESSAGE);
            entradanombre.setText(nombre);//Se regresa al instante antes de poner el caracteri ilegal
        }
    }//GEN-LAST:event_entradanombreKeyPressed

